# ==========Numeric Datatype ==========

#==int==#
a=1532
print(type(a))

#==float==#
b=15.32
print(type(b))

#==complex==#
c=15j+32
print(type(c))


# ==========Sequence Datatype ==========

#==str==#
name='dhruvin virani'
print(type(name))

#==List==#
num=["1532","7640","5229","5792","0032"]
print(type(num))

#==tuple==#
subject=("python","jav","btech","html","css")
print(type(subject))

#==boolean==#
t=True
print(type(t))

f=False
print(type(f))

#==set==#
v={"python","jav","a","a"}
print(type(v))

#==map(dict)==#
n={"name":"dhruvin","roll":"1"}
print(type(n))



